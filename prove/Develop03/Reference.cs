using System;

class Reference
{
    /// This property contains the name of book related to the current reference
    public string Book {get; set;}
    /// This property contains the chapter number related to the current reference
    public int Chapter { get; set; }
    /// This property contains the start verse number related to the current reference
    public int StartVerse { get; set; }
    /// This property contains the end verse related to the current reference
    public int EndVerse { get; set; }
    /// This property contains the text related to the current reference
    public string Text {get; set; }
}